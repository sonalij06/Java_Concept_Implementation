public class LargestNumber {
    public static void main(String[] args)
    {
        //Varibles
        int x=10,y=5,z=12;
        if(x > y && x > z)
            System.out.println("Largest Number is x : " + x);
        else if(y > x && y > z)
            System.out.println("Largest Number is y : " + y);
        else
            System.out.println("Largest Number is z : " + z);

    }
}
