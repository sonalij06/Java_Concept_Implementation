public class MaleFemale {
    public static void main(String[] args)
    {
        //Variables
        char gender = 'M';
        switch (gender)
        {
            case 'm':
            case 'M':
            {
                System.out.println("Male");
                break;
            }
            case 'f':
            case 'F':
            {
                System.out.println("Female");
                break;
            }
        }
    }
}
