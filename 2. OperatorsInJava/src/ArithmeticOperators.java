public class ArithmeticOperators {
    public static void main(String[] args)
    {
        //Variables
        double a=10,b=100;

        //Addition Operator ( + )
        System.out.println("a + b = "+(a+b));

        //Subtraction Operator ( - )
        System.out.println("a - b = "+(a-b));

        //Multiplication Operator ( * )
        System.out.println("a x b = "+(a*b));

        //Division Operator ( / )
        System.out.println("a / b = "+(double)(a/b));
    }
}
