public class CommentsInJava {
    public static void main(String[] args)
    {

        //This is a single line comment
        System.out.println("Single-line comment is written by '//'");


        /*This is multi-line comment
        1.First line
        2.Second line
         */
        System.out.println("Multi-line comment is written by '/* --------- */'");


        /*
         * <h1>This is to show comments in Java</h1>
         * 1.Single-line comment.
         * 2.Multi-line comment.
         * documentation comment.
         * @author Sonali Jain
         * @since 25-08-2021
         * @version 1.0
         */
        System.out.println("Documentation comment is written by '/* ***** */'");
    }
}
